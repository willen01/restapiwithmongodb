package com.willen.restapiwithmongodb.services;

import java.util.Optional;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import com.willen.restapiwithmongodb.domain.Post;
import com.willen.restapiwithmongodb.repository.PostRepository;
import com.willen.restapiwithmongodb.services.exception.ObjectNotFoundException;

@Service
public class PostService {

    @Autowired
    private PostRepository repo;

    public Post findById(String id) {
        Optional<Post> post = repo.findById(id);
        return post.orElseThrow(() -> new ObjectNotFoundException("Objeto n√£o encontrado"));
    }

}
